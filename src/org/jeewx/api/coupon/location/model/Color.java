package org.jeewx.api.coupon.location.model;

public class Color {
	//颜色名称
	private String name ;
	//颜色值
	private String value ;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}
	
	
	
}
