package org.jeewx.api.wxstore.product.model;

public class PropertiesValue {
	// 属性值id
	private String id;
	// 属性值名称
	private String name;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
}
